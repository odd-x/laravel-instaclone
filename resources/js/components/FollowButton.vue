<template>
  <div>
    <button class="btn btn-primary ml-4" user-id="this.userId" @click="followUser">Follow</button>
  </div>
</template>

<script>
export default {
  props: ["userId"],

  mounted() {
    console.log("Component mounted.");
  },

  methods: {
    followUser() {
      axios.post("/follow/" + this.userId).then(Response => {
        alert(Response.data);
      });
    }
  }
};
</script>
